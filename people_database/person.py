class Person:
    def __init__(self, first, last, em, st):
        self.first_name = first
        self.last_name = last
        self.email = em
        self.state = st

    def first_name():
        return self.first_name


    def last_name():
        return self.last_name

    def email():
        return self.email

    def state():
        return self.state
